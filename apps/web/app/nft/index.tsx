const contractAddress = '0xd9145CCE52D386f254917e481eB44e9943F39138';

// Otherwise typescript will give an error
declare global {
  interface Window {
    ethereum: any;
  }
}

export function NftMinter() {
  const [provider, setProvider] = useState<any>(null);
  const [contract, setContract] = useState<any>(null);

  useEffect(() => {
    if (window.ethereum) {
      setProvider(new ethers.JsonRpcProvider(window.ethereum));
    } else {
      alert('Please install MetaMask to use this application');
    }
  }, []);

  useEffect(() => {
    if (provider) {
      setContract(new ethers.Contract(contractAddress, contractAbi, provider.getSigner()));
    }
  }, [provider]);

async function requestAccount() {
	if (window.ethereum) {
	  try {
		// Request account access
		await window.ethereum.request({ method: 'eth_requestAccounts' });
	  } catch (error) {
		// User denied account access
		console.error("User denied account access");
	  }
	} 
	else {
	  console.log("Non-Ethereum browser detected. Consider installing MetaMask!");
	}
  }

  async function createNft(data: any) {
    if (contract) {
      // Save the data to IPFS
      const ipfsResult = await ipfs.add(JSON.stringify(data));
      const ipfsHash = ipfsResult.path;

	  //Ask for the recipient address
	  requestAccount();
      // Mint the NFT
      const tx = await contract.safeMint(window.ethereum.selectedAddress, ipfsHash);
      await tx.wait();
      return ipfsHash;
    } else {
      alert('Contract is not loaded');
    }
  }

  async function handleMintNft() {
    const data = {"name": "Test Scan",
    "result": "clean",
    "date": "2023-05-25"};
    const result = await createNft(data);
    console.log('NFT minted: ', result);
  }

  return (
    <button onClick={handleMintNft}>Mint NFT</button>
  );
}
